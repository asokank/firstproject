package oopsConcets;

public class Battery {
	private String brand;
	private int volts;
	private boolean isRechargeble;
	public void setbrand(String brand) {
		this.brand=brand;
	}
	public String getbrand() {
		return brand;
	}
	public void setvolts(int volts) {
		this.volts=volts;
	}
	public int getvolts() {
		return volts;
	}
	public void setisRechargeble(boolean isRechargeble) {
		this.isRechargeble=isRechargeble;
	}
	public boolean getisRechargeble() {
		return isRechargeble;
	}
	
	

}
